package com.mikealbert.data.vo;

public class ClientPocVO {
	
	String pocCategory;
	String pocName;
	String pocDescription;
	String contactName;
	String sendToDriver;
	
	public String getPocCategory() {
		return pocCategory;
	}
	public void setPocCategory(String pocCategory) {
		this.pocCategory = pocCategory;
	}
	public String getPocName() {
		return pocName;
	}
	public void setPocName(String pocName) {
		this.pocName = pocName;
	}
	public String getPocDescription() {
		return pocDescription;
	}
	public void setPocDescription(String pocDescription) {
		this.pocDescription = pocDescription;
	}
	public String getContactName() {
		return contactName;
	}
	public void setContactName(String contactName) {
		this.contactName = contactName;
	}
	public String getSendToDriver() {
		return sendToDriver;
	}
	public void setSendToDriver(String sendToDriver) {
		this.sendToDriver = sendToDriver;
	}
	
}
