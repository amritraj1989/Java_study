package com.mikealbert.data.vo;

public class ModelSearchCriteriaVO {
	private String year;
	private String make;
	private String model;
	private String modelCode;
	private String trim;
	private String modelType;
	private String mfgCode;
	private String lovSource;
		
	public ModelSearchCriteriaVO(){}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getMake() {
		return make;
	}

	public void setMake(String make) {
		this.make = make;
	}

	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public String getModelCode() {
		return modelCode;
	}

	public void setModelCode(String modelCode) {
		this.modelCode = modelCode;
	}

	public String getTrim() {
		return trim;
	}

	public void setTrim(String trim) {
		this.trim = trim;
	}

	public String getModelType() {
		return modelType;
	}

	public void setModelType(String modelType) {
		this.modelType = modelType;
	}

	public String getMfgCode() {
		return mfgCode;
	}

	public void setMfgCode(String mfgCode) {
		this.mfgCode = mfgCode;
	}

	public String getLovSource() {
		return lovSource;
	}

	public void setLovSource(String lovSource) {
		this.lovSource = lovSource;
	}
	

}
